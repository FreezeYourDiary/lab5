"""module docstring"""


def add(a: int, b: int) -> int:
    """Return sum of a b."""
    return a + b


def subtract(a: int, b: int) -> int:
    """Return diff of a and b."""
    return a - b


def multiply(a: int, b: int) -> int:
    """Return the product of a and b."""
    return a * b


def divide(a: int, b: int) -> float:
    """Return the quotient of a divided by b."""
    return a / b
